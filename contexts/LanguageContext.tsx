const LanguageContext = createContext<LanguageContextType | undefined>(
  undefined
);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>("device");

  // Get the resolved language (actual language code used for translations)
  // Memoize to avoid recalculating device language on every render
  const resolvedLanguage: ResolvedLanguage = useMemo(() => {
    return language === "device" ? getDeviceLanguage() : language;
  }, [language]);

  useEffect(() => {
    loadLanguage();
  }, []);

  const loadLanguage = async () => {
    try {
      const saved = await AsyncStorage.getItem(STORAGE_KEYS.LANGUAGE);
      if (saved && SUPPORTED_LANGUAGES.includes(saved as Language)) {
        setLanguageState(saved as Language);
      } else {
        setLanguageState("device");
      }
    } catch (error) {
      logger.error("Failed to load language", error);
      setLanguageState("device");
    }
  };

  const setLanguage = async (lang: Language) => {
    try {
      await AsyncStorage.setItem(STORAGE_KEYS.LANGUAGE, lang);
      setLanguageState(lang);
    } catch (error) {
      logger.error("Failed to save language", error);
    }
  };

  const t = useMemo(
    () =>
      (key: string): string => {
        return (
          translations[resolvedLanguage]?.[key] ||
          GLOSSARY_TRANSLATIONS[resolvedLanguage]?.[key] ||
          translations.en[key] ||
          GLOSSARY_TRANSLATIONS.en[key] ||
          key
        );
      },
    [resolvedLanguage]
  );

  const contextValue = useMemo(
    () => ({ language, resolvedLanguage, setLanguage, t }),
    [language, resolvedLanguage, t],
  );

  return (
    <LanguageContext.Provider value={contextValue}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within LanguageProvider");
  }
  return context;
}
